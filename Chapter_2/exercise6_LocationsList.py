print("The length of the list is:", len(locations))

sorted(locations)
print(locations)

sorted_locations = sorted(locations, reverse=False)
print(sorted_locations)

sorted_loc = sorted(locations, reverse=True)
print(sorted_loc)

locations.sort()
print(locations)

locations.sort(reverse=True)
print(locations)

